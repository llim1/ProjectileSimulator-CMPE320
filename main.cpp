// Main code
int main(int, char**)
{
    // Create application window
    //ImGui_ImplWin32_EnableDpiAwareness();
    WNDCLASSEX wc = { sizeof(WNDCLASSEX), CS_CLASSDC, WndProc, 0L, 0L, GetModuleHandle(NULL), NULL, NULL, NULL, NULL, _T("ImGui Example"), NULL };
    ::RegisterClassEx(&wc);
    HWND hwnd = ::CreateWindow(wc.lpszClassName, _T("Projectile Simulator"), WS_OVERLAPPEDWINDOW, 100, 100, 750, 500, NULL, NULL, wc.hInstance, NULL);

    // Initialize Direct3D
    if (!CreateDeviceD3D(hwnd))
    {
        CleanupDeviceD3D();
        ::UnregisterClass(wc.lpszClassName, wc.hInstance);
        return 1;
    }

    // Show the window
    ::ShowWindow(hwnd, SW_SHOWDEFAULT);
    ::UpdateWindow(hwnd);

    // Setup Dear ImGui context
    IMGUI_CHECKVERSION();
    ImGui::CreateContext();
    ImGuiIO& io = ImGui::GetIO(); (void)io;
    //io.ConfigFlags |= ImGuiConfigFlags_NavEnableKeyboard;     // Enable Keyboard Controls
    //io.ConfigFlags |= ImGuiConfigFlags_NavEnableGamepad;      // Enable Gamepad Controls

    // Setup Dear ImGui style
    //ImGui::StyleColorsDark();
    ImGui::StyleColorsClassic();

    // Setup Platform/Renderer backends
    ImGui_ImplWin32_Init(hwnd);
    ImGui_ImplDX11_Init(g_pd3dDevice, g_pd3dDeviceContext);

    // Our state
    bool show_demo_window = true;
    bool show_another_window = false;
    ImVec4 clear_color = ImVec4(0.45f, 0.55f, 0.60f, 1.00f);

    // Main loop
    MSG msg;
    ZeroMemory(&msg, sizeof(msg));
    while (msg.message != WM_QUIT)
    {
        // Poll and handle messages (inputs, window resize, etc.)
        // You can read the io.WantCaptureMouse, io.WantCaptureKeyboard flags to tell if dear imgui wants to use your inputs.
        // - When io.WantCaptureMouse is true, do not dispatch mouse input data to your main application.
        // - When io.WantCaptureKeyboard is true, do not dispatch keyboard input data to your main application.
        // Generally you may always pass all inputs to dear imgui, and hide them from your application based on those two flags.
        if (::PeekMessage(&msg, NULL, 0U, 0U, PM_REMOVE))
        {
            ::TranslateMessage(&msg);
            ::DispatchMessage(&msg);
            continue;
        }

        // Start the Dear ImGui frame
        ImGui_ImplDX11_NewFrame();
        ImGui_ImplWin32_NewFrame();
        ImGui::NewFrame();

        // Begin/End pair to created a named window.
        {
            static int i = 7;
            static int counter = 0;

            int image_width = 0;
            int image_height = 0;
            ID3D11ShaderResourceView* my_texture = NULL;
            bool ret = LoadTextureFromFile("space.png", &my_texture, &image_width, &image_height);
            IM_ASSERT(ret);

            ImGui::SetNextWindowPos(ImVec2(-10, -10));
            ImGui::SetNextWindowSize(ImVec2(800, 500), 0);
            ImGui::Begin("Projectile Simulator", NULL, 1);

            //display space background
            ImGui::Image((void*)my_texture, ImVec2(800, 500));

            //display jupiter
            ret = LoadTextureFromFile("Jupiter.png", &my_texture, &image_width, &image_height);
            IM_ASSERT(ret);
            ImGui::SetCursorPos(ImVec2(0, 400));
            ImGui::Image((void*)my_texture, ImVec2(750, image_height));

            ImGui::SetCursorPos(ImVec2(350, 440));
            ImGui::PushStyleColor(ImGuiCol_Text, ImVec4(ImColor(0,0,0)));
            ImGui::Text("Jupiter");
            ImGui::PopStyleColor();

            //display cannon
            ret = LoadTextureFromFile("cannon.png", &my_texture, &image_width, &image_height);
            IM_ASSERT(ret);
            ImGui::SetCursorPos(ImVec2(0, 350));
            ImGui::Image((void*)my_texture, ImVec2(image_width/2, image_height/2));

            //Wind Control
            ImGui::SetCursorPos(ImVec2(410, 40));
            ImGui::PushItemWidth(230);
            ImGui::SliderInt("Wind(m/s)", &i, -10, 10);            // Edit wind using a slider from -10 to 10
            ImGui::PopItemWidth();

            //Home button
            ImGui::SetCursorPos(ImVec2(40, 40));
            ImGui::Button("Home", ImVec2(75, 30));

            //Back button
            ImGui::SetCursorPos(ImVec2(40, 80));
            ImGui::Button("Back", ImVec2(75, 30));

            //Launch button
            ImGui::SetCursorPos(ImVec2(630, 80));
            ImGui::PushStyleColor(ImGuiCol_Button, ImVec4(ImColor(225, 44, 3)));
            ImGui::Button("LAUNCH", ImVec2(85, 40));
            ImGui::PopStyleColor();

            //Compare button
            ImGui::SetCursorPos(ImVec2(640, 130));
            if (ImGui::Button("Compare", ImVec2(75, 30))){
                ImGui::OpenPopup("Compare Projectile Paths");
            }

            ImVec2 center(ImGui::GetIO().DisplaySize.x * 0.5f, ImGui::GetIO().DisplaySize.y * 0.5f);
            ImGui::SetNextWindowPos(center, ImGuiCond_Appearing, ImVec2(0.5f, 0.5f));
            ImGui::SetNextWindowSize(ImVec2(400, 180));
            if (ImGui::BeginPopupModal("Compare Projectile Paths"))
            {
                ImGui::Text("Choose the projectiles you want to compare:");
                ImGui::Separator();
                static bool projectileA = false;
                static bool projectileB = false;
                static bool projectileC = false;
                ImGui::Checkbox("Projectile A", &projectileA);
                ImGui::Checkbox("Projectile B", &projectileB);
                ImGui::Checkbox("Projectile C", &projectileC);

                ImGui::Separator();
                ImGui::SetCursorPos(ImVec2(100, 150));
                if (ImGui::Button("Close", ImVec2(100, 0))) { ImGui::CloseCurrentPopup(); }
                ImGui::SameLine();
                if (ImGui::Button("Compare", ImVec2(100, 0))) { ImGui::CloseCurrentPopup(); }
                ImGui::EndPopup();
            }

            ImGui::End();
        }

        // Rendering
        ImGui::Render();
        g_pd3dDeviceContext->OMSetRenderTargets(1, &g_mainRenderTargetView, NULL);
        g_pd3dDeviceContext->ClearRenderTargetView(g_mainRenderTargetView, (float*)&clear_color);
        ImGui_ImplDX11_RenderDrawData(ImGui::GetDrawData());

        g_pSwapChain->Present(1, 0); // Present with vsync
        //g_pSwapChain->Present(0, 0); // Present without vsync
    }

    // Cleanup
    ImGui_ImplDX11_Shutdown();
    ImGui_ImplWin32_Shutdown();
    ImGui::DestroyContext();

    CleanupDeviceD3D();
    ::DestroyWindow(hwnd);
    ::UnregisterClass(wc.lpszClassName, wc.hInstance);

    return 0;
}

